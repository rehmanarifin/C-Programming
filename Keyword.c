/*An integer variable number that will contain 10,
write a program that prints an integer number
*/
#include<stdio.h>
int main ()
{
   int num =10;
   float num1 = 10.10;
   int num2=20;
   double num3=10.1000005;
   char ch='R';
   printf("Number is integer=%d\n",num);
   printf("number is float=%f\n ",num1);
   printf("number is integer =%d\n",num2);
   printf("number is double=%lf\n",num3);
   printf("ch=%c\n",ch);
   return 0;
}
